        <h1><strong>TABLE TD TR</strong></h1>
<h1><em>Définitions:</em></h1>
<p>
<em>Balise de tableau, qui est définie avec la balise (table). Chaque ligne du tableau est définie avec la balise (tr). Un en-tête de table est défini avec la balise (th). Par défaut, les en-têtes de table sont en gras et centrés.</em>
&nbsp;
