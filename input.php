        <h1>INPUT</h1>
<h1><em>Définition:</em></h1>
<p>
<em>La balise <strong>INPUT</strong> désigne une zone d'entrée où un utilisateur peut entrer des données</em><br>
<em>Il est utilisé dans un élément <strong>form</strong> en indiquant de quelle manière l'utilisateur peut entrer des données</xp></em><br>
<em>Les données que l'on ajoute peuvent être divers.</em>

</p>    
            </article>

            <article class="exemple">
<h1><em>Exemple:</em></h1>
    <em><xmp>    <form action="/action_page.php">

        First name: <input type="text" name="fname"><br>
        
        Last name: <input type="text" name="lname"><br>
        <input type="submit" value="Submit">
      </form></xmp></em>
